// 決定論的な擬似乱数 (mulberry32)
function rng(seed) {
  let a = seed >>> 0;
  return () => {
    a |= 0; a = (a + 0x6d2b79f5) | 0;
    let t = Math.imul(a ^ (a >>> 15), 1 | a);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

function isWeekend(d) { const w = d.getUTCDay(); return w === 0 || w === 6; }
function iso(d) { return d.toISOString().slice(0, 10); }
function parseDate(s) { return new Date(s + "T00:00:00Z"); }

/** start(含む)〜end(含む)の営業日(土日除く)リスト */
function businessDays(start, end) {
  const days = [];
  for (let d = parseDate(start); d <= parseDate(end); d = new Date(d.getTime() + 86400000)) {
    if (!isWeekend(d)) days.push(iso(d));
  }
  return days;
}

function generate() {
  // アンカー検証
  for (let k = 0; k < ANCHORS.length; k++) {
    const [date] = ANCHORS[k];
    if (isWeekend(parseDate(date))) throw new Error(`アンカーが週末です: ${date}`);
    if (k > 0 && ANCHORS[k - 1][0] >= date) throw new Error(`アンカーの順序が不正: ${date}`);
  }

  const rand = rng(20250707);
  const rows = []; // [date, close]

  for (let k = 0; k < ANCHORS.length - 1; k++) {
    const [d0, v0] = ANCHORS[k];
    const [d1, v1] = ANCHORS[k + 1];
    const days = businessDays(d0, d1);
    if (days[0] !== d0 || days[days.length - 1] !== d1) throw new Error(`アンカー日が営業日列に一致しません: ${d0}〜${d1}`);
    const n = days.length - 1; // セグメント内のステップ数
    for (let j = 0; j < n; j++) {
      // j=0 はアンカー当日 (正確な値)。中間日は対数線形補間 + 端点で0になるノイズ
      const t = j / n;
      const logv = Math.log(v0) + (Math.log(v1) - Math.log(v0)) * t;
      const noiseAmp = 0.004 * Math.sin(Math.PI * t); // 端点で0
      const noise = (rand() * 2 - 1) * noiseAmp;
      rows.push([days[j], Math.round(Math.exp(logv + noise) * 100) / 100]);
    }
  }
  rows.push(ANCHORS[ANCHORS.length - 1]);

  const lines = ["Date,Open,High,Low,Close,Volume"];
  for (const [date, close] of rows) {
    lines.push(`${date},${close},${close},${close},${close},0`);
  }
  fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true });
  fs.writeFileSync(OUT_PATH, lines.join("\n") + "\n", "utf8");

  // 想定外の単日暴落(アンカー由来でない -4%超)が無いか自己検査
  let prev = null, artifacts = 0;
  const anchorSet = new Set(ANCHORS.map((a) => a[0]));
  for (const [date, close] of rows) {
    if (prev && close / prev - 1 <= -0.04 && !anchorSet.has(date)) artifacts++;
    prev = close;
  }
  console.log(`生成完了: ${OUT_PATH} (${rows.length}営業日, 補間起因の-4%超下落: ${artifacts}件)`);
  if (artifacts > 0) console.warn("⚠ 補間による想定外の暴落日があります。アンカーを見直してください。");
}

generate();
